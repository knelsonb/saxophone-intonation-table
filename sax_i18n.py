def available_languages():
    """Return the list of language codes that the table currently covers."""
    return list(STRINGS.keys())


def keys():
    """Return the union of string keys across all languages.

    Useful for tooling that wants to detect translation gaps without
    knowing which language is "primary".
    """
    seen = set()
    for table in STRINGS.values():
        seen.update(table.keys())
    return seen
